int	ft_is_prime(int nb)
{
	int	loop;

	loop = 2;
	if (nb < 2)
		return (0);
	while (loop < nb && loop <= 46340)
	{
		if (nb % loop == 0)
			return (0);
		loop++;
	}
	return (1);
}
