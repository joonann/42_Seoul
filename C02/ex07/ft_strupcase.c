char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	if (str[0] == 0)
		return (str);
	while (str[i])
	{
		if (str[i] <= 'z' && str[i] >= 'a')
		{	
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
